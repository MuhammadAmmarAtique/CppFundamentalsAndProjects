#include <iostream>

using namespace std;

int main()

{
int a,b;

cout<<"Enter first number:"<<endl;
cin>>a;
cout<<"Enter second number:"<<endl;
cin>>b;

cout<<"The sum of number is:";             //ADD
cout<<a+b<<endl;

cout<<"The difference of number is:";     //DIFFERENCE
cout<<a-b<<endl;

cout<<"The multiply of number is:";       //Multiply
cout<<a*b<<endl;

cout<<"The division of number is:";       //Divide
cout<<(float)a/b<<endl;                                       //Type casting

return 0;

}